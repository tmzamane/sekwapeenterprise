<script>
    AOS.init({
        duration: 800,
        easing: 'ease-in-out',
    });

    document.addEventListener('DOMContentLoaded', function() {
        const whatsappLinkContainer = document.querySelector('.sekwape-whatsapp-link-container');
        let timeoutId;

        function hideOnScroll() {
            whatsappLinkContainer.classList.add('hidden');
        }

        function showAfterScrollStop() {
            whatsappLinkContainer.classList.remove('hidden');
        }

        window.addEventListener('scroll', function() {
            hideOnScroll();
            clearTimeout(timeoutId);
            timeoutId = setTimeout(showAfterScrollStop, 300);
        });

        // FAQ Accordion Logic
        const faqQuestions = document.querySelectorAll('.faq-question');
        faqQuestions.forEach(question => {
            question.addEventListener('click', function() {
                this.classList.toggle('active');
                const answer = this.nextElementSibling;
                answer.classList.toggle('active');
            });
        });

        // --- Intersection Observer for Animated Grid (REFACTORED) ---
        const fleetGrid = document.querySelector('.animated-grid');
        if (fleetGrid) {
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        fleetGrid.classList.add('loaded');
                        observer.unobserve(fleetGrid); // Animate only once
                    }
                });
            }, {
                threshold: 0.5 // Trigger when 50% of the grid is visible
            });

            observer.observe(fleetGrid);
        }

        // Basic FAQ accordion (if you want this functionality)
        const faqItems = document.querySelectorAll('.faq-item');
        faqItems.forEach(item => {
            const question = item.querySelector('.faq-question');
            const answer = item.querySelector('.faq-answer');

            question.addEventListener('click', () => {
                answer.style.display = answer.style.display === 'block' ? 'none' : 'block';
                question.classList.toggle('active');
            });
        });
    });
</script>
